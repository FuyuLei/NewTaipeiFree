package comn.example.user.newtaipeifree.model;

public class Place {
    private String id;
    private String spot_name;
    private String type;
    private String company;
    private String district;
    private String address;
    private String apparatus_name;
    private String latitude;
    private String longitude;
    private String twd97X;
    private String twd97Y;
    private String wgs84aX;
    private String wgs84aY;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSpot_name() {
        return spot_name;
    }

    public void setSpot_name(String spot_name) {
        this.spot_name = spot_name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getApparatus_name() {
        return apparatus_name;
    }

    public void setApparatus_name(String apparatus_name) {
        this.apparatus_name = apparatus_name;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getTwd97X() {
        return twd97X;
    }

    public void setTwd97X(String twd97X) {
        this.twd97X = twd97X;
    }

    public String getTwd97Y() {
        return twd97Y;
    }

    public void setTwd97Y(String twd97Y) {
        this.twd97Y = twd97Y;
    }

    public String getWgs84aX() {
        return wgs84aX;
    }

    public void setWgs84aX(String wgs84aX) {
        this.wgs84aX = wgs84aX;
    }

    public String getWgs84aY() {
        return wgs84aY;
    }

    public void setWgs84aY(String wgs84aY) {
        this.wgs84aY = wgs84aY;
    }

}
